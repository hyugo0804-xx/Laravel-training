     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $todos = new Todos();
        $todos->title = $request->input('title');
        $todos->save();

        return redirect('todos')->with(
            'status',
            $todos->title . 'を登録しました'
        );
    }

